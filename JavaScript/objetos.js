// Criação de Objeto no JavaScript
const mutante = {
    nome: `Geralt of Rivia`,
    idade: undefined,
    peso: undefined,
    bruxo: true
}

// Imprimindo o objeto
console.log(mutante);

// Selecionando atributos do objeto para impressão
console.log(`Qual a idade de ${mutante.nome}? ${mutante.idade}`);

// Aplicando uma Estrutura Condicional (If.. else..) no atributo boolean do objeto
if (mutante.bruxo = true) {
    console.log(`Qual o nome do mutante? ${mutante.nome}. Ele é um bruxo ? Correto`);
} else {
    console.log(`Qual o nome do mutante? ${mutante.nome}. Ele é um bruxo ? Falso`);
};


